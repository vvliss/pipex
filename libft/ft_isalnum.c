#include "libft.h"

int	ft_isalnum(int digi)
{
	if ((digi >= '0' && digi <= '9')
		|| (digi >= 'A' && digi <= 'Z')
		|| (digi >= 'a' && digi <= 'z'))
		return (1);
	return (0);
}
